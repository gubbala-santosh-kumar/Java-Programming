// Data Types
public class Main {  
    public static void main(String[] args)  
    {  
        int Num1 = 10;
        System.out.println(Num1);
        
        // float Num2 = 2;  Syntax to declare a float value
        float Num2 = 2.0f;  
        System.out.println(Num2);  

        double Num3 = 75.658;  
        System.out.println(Num3);

        char letter = 'A';
        System.out.println(letter);

        String Name= "Santosh";
        System.out.println(Name);

        // long, short, byte these are also data types
    }  
} 
