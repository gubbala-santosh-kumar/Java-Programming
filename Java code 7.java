// Java Math
public class Main
{
	public static void main(String[] args) {
	    int num1 = 49;
	    int num2 = 1;
	    System.out.println(Math.max(num1,num2)); // Math.max()
	    System.out.println(Math.min(num1,num2)); // Math.min()
		System.out.println((int)Math.sqrt(num1)); // Math.sqrt()
		System.out.println((int)(Math.random()*10)); // Math.random()
	}
}
